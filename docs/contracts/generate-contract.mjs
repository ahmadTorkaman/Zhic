// Helper: RTL paragraph
const rtl = (text, opts = {}) => new Paragraph({
  alignment: opts.alignment || AlignmentType.RIGHT,
  bidirectional: true,
  spacing: { after: opts.afterSpacing ?? 120, before: opts.beforeSpacing ?? 0 },
  heading: opts.heading,
  children: [
    new TextRun({
      text,
      font: "B Nazanin",
      size: opts.size || 24,
      bold: opts.bold || false,
      rightToLeft: true,
    }),
  ],
});

// Helper: Bold + normal text in one paragraph
const rtlMixed = (parts, opts = {}) => new Paragraph({
  alignment: opts.alignment || AlignmentType.RIGHT,
  bidirectional: true,
  spacing: { after: opts.afterSpacing ?? 120 },
  children: parts.map(p => new TextRun({
    text: p.text,
    font: "B Nazanin",
    size: p.size || 24,
    bold: p.bold || false,
    rightToLeft: true,
  })),
});

// Helper: table cell
const cell = (text, opts = {}) => new TableCell({
  children: [rtl(text, { size: opts.size || 22, afterSpacing: 40 })],
  width: opts.width ? { size: opts.width, type: WidthType.PERCENTAGE } : undefined,
  shading: opts.shading ? { type: ShadingType.SOLID, color: opts.shading } : undefined,
});

// Helper: header row
const headerRow = (texts, widths) => new TableRow({
  children: texts.map((t, i) => cell(t, {
    bold: true,
    shading: "D9E2F3",
    width: widths?.[i],
    size: 22,
  })),
});

// Helper: data row
const dataRow = (texts, widths) => new TableRow({
  children: texts.map((t, i) => cell(t, { width: widths?.[i], size: 22 })),
});

// Helper: simple table
